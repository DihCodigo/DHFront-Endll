const somar = (x, y) => {
    return x + y;
}

const subtrair = (x, y) => {
    return x - y;
}

const multiplicar = (x, y) => {
    return x * y;
}

const dividir = (x, y) => {
    return x / y;
}

console.log(somar(2, 4));
console.log(subtrair(2, 4));
console.log(multiplicar(2, 4));
console.log(dividir(2, 4));